
public class calculator1 {
    public static void main(String args[]) {

        for (int i = 1; i <= 5; i++) {
            int count = 1;

            for (int j = 0; j < i; j++) {

                System.out.print(count);
                count += 1;
            }
            System.out.println("*");

        }
    }

}
